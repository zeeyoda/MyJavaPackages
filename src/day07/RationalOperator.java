package day07;

public class RationalOperator {

    public static void main(String[] args) {

        // > >= < <= == !=

        //Relation Operator is used to compare 2 operands
        //and return true or false
        //anything action or expression
        //that return true or false
        //can be called boolean expression

        System.out.println(5>3);
        System.out.println(7< -19);
        System.out.println(5>=5);
        System.out.println(7>8);
        System.out.println(4<=5);
        System.out.println(17>=8);

        System.out.println(5==5);
        //== is used to check for equality
        //AND VERY DIFFERENT FROM SINGLE =

        // difference between == and == same as difference between equal
        // and equality in daily English

        // != is used to check for inequality, ! is translated as NOT in programming
        System.out.println(6 != 10);

        // == and != are questions. checking for equality or inequality

    }
}
